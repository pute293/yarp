module PDF::Utils::Encoding
  
  # PDFDocEncoding is 1 char/byte encoding,
  # (used for only internal pdf program)
  # same as Latin-1 for range 0x20-0x7e.
  # Strings in the dictionary (not stream) are
  # encoded this form or UTF-16BE(with BOM \xfe\xff).
  # This table is map from PDFDocEncoding-encoded value (\x00-\xff) to Unicode codepoint,
  # created from PDF 1.7 spec Table D.2.
  # (ref. ISO 32000-1 Standards;
  #    table D.2 (pp. 656-664)
  #    http://wwwimages.adobe.com/content/dam/Adobe/en/devnet/pdf/pdfs/PDF32000_2008.pdf).
  PDFDocEncoding = Hash[
    [ # 00 - 07 is not defined; but Adobe Reader handle 00 as NUL
      0,      nil,    nil,    nil,    nil,    nil,    nil,    nil,    # 00
      # 09, 0a, 0d is same as Ascii; not defined else                 #   
      nil,    0x0009, 0x000a, nil,    nil,    0x000d, nil,    nil,    #   
      # 10 - 17 is not defined                                        #   
      nil,    nil,    nil,    nil,    nil,    nil,    nil,    nil,    # 10
      # 18 - 1f is used for special chars                             #   
      0x02d8, 0x02c7, 0x02c6, 0x02d9, 0x02dd, 0x02db, 0x02da, 0x02dc, #   
      # 20 - 7e is same as Latin-1/Ascii; 7f is not defined           #   
      0x20,   0x21,   0x22,   0x23,   0x24,   0x25,   0x26,   0x27,   # 20
      0x28,   0x29,   0x2a,   0x2b,   0x2c,   0x2d,   0x2e,   0x2f,   #   
      0x30,   0x31,   0x32,   0x33,   0x34,   0x35,   0x36,   0x37,   # 30
      0x38,   0x39,   0x3a,   0x3b,   0x3c,   0x3d,   0x3e,   0x3f,   #   
      0x40,   0x41,   0x42,   0x43,   0x44,   0x45,   0x46,   0x47,   # 40
      0x48,   0x49,   0x4a,   0x4b,   0x4c,   0x4d,   0x4e,   0x4f,   #   
      0x50,   0x51,   0x52,   0x53,   0x54,   0x55,   0x56,   0x57,   # 50
      0x58,   0x59,   0x5a,   0x5b,   0x5c,   0x5d,   0x5e,   0x5f,   #   
      0x60,   0x61,   0x62,   0x63,   0x64,   0x65,   0x66,   0x67,   # 60
      0x68,   0x69,   0x6a,   0x6b,   0x6c,   0x6d,   0x6e,   0x6f,   #   
      0x70,   0x71,   0x72,   0x73,   0x74,   0x75,   0x76,   0x77,   # 70
      0x78,   0x79,   0x7a,   0x7b,   0x7c,   0x7d,   0x7e,   nil,    #   
      # 80 - 9e is used for special chars; 9f is not defined          #   
      0x2022, 0x2020, 0x2021, 0x2026, 0x2014, 0x2013, 0x0192, 0x2044, # 80
      0x2039, 0x203a, 0x2212, 0x2030, 0x201e, 0x201c, 0x201d, 0x2018, #   
      0x2019, 0x201a, 0x2122, 0xfb01, 0xfb02, 0x0141, 0x0152, 0x0160, # 90
      0x0178, 0x017d, 0x0131, 0x0142, 0x0153, 0x0161, 0x017e, nil,    #   
      # a0 - ff is same as Latin-1, except for a0 and ad              #   
      # a0 is EURO SIGN and ad is not defined.                        #   
      0x20ac, 0xa1,   0xa2,   0xa3,   0xa4,   0xa5,   0xa6,   0xa7,   # A0
      0xa8,   0xa9,   0xaa,   0xab,   0xac,   nil,    0xae,   0xaf,   #   
      0xb0,   0xb1,   0xb2,   0xb3,   0xb4,   0xb5,   0xb6,   0xb7,   # B0
      0xb8,   0xb9,   0xba,   0xbb,   0xbc,   0xbd,   0xbe,   0xbf,   #   
      0xc0,   0xc1,   0xc2,   0xc3,   0xc4,   0xc5,   0xc6,   0xc7,   # C0
      0xc8,   0xc9,   0xca,   0xcb,   0xcc,   0xcd,   0xce,   0xcf,   #   
      0xd0,   0xd1,   0xd2,   0xd3,   0xd4,   0xd5,   0xd6,   0xd7,   # D0
      0xd8,   0xd9,   0xda,   0xdb,   0xdc,   0xdd,   0xde,   0xdf,   #   
      0xe0,   0xe1,   0xe2,   0xe3,   0xe4,   0xe5,   0xe6,   0xe7,   # E0
      0xe8,   0xe9,   0xea,   0xeb,   0xec,   0xed,   0xee,   0xef,   #   
      0xf0,   0xf1,   0xf2,   0xf3,   0xf4,   0xf5,   0xf6,   0xf7,   # F0
      0xf8,   0xf9,   0xfa,   0xfb,   0xfc,   0xfd,   0xfe,   0xff,   #   
    ].each_with_index.collect{|x,i| [i.chr, x ? x : 0]}
  ].freeze
  
end
